package org.feber.tp.modul7.nomor4;

/**
 *
 * @author feber
 */
public class Kucing {

    private String nama, panggilan;

    public Kucing(String nama, String panggilan) {
        this.nama = nama;
        this.panggilan = panggilan;
    }

    public String getNama() {
        return nama;
    }

    public String getPanggilan() {
        return panggilan;
    }
}
